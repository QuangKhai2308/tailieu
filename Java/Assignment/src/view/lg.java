    private boolean checkNull() {
        if (txtUser.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "User chưa được nhập");
            txtUser.requestFocus();
            return false;
        } else if (txtPass.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Password chưa được nhập");
            txtPass.requestFocus();
            return false;
        }
        return true;
    }

    protected boolean checkUse() {
        String query = "SELECT * FROM USERS WHERE username = ? AND password = ?";
        boolean check = false;
        try {
            PreparedStatement ps = conn.prepareStatement(query);
            ResultSet rs;

            ps.setString(1, txtUser.getText());
            ps.setString(2, String.valueOf(txtPass.getPassword()));

            rs = ps.executeQuery();
            if (rs.next()) {
                check = true;
                role = rs.getString("role");
//                System.out.println(role);
            } else {
                JOptionPane.showMessageDialog(this, "username or password không đúng");
                check = false;
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        return check;
    }
