    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String UserName = txtUsername.getText();
        String Password = txtPassword.getText();
        if(UserName.equalsIgnoreCase("Poly") || Password.equalsIgnoreCase("Polytechnic")){
            JOptionPane.showMessageDialog(this, "Đăng nhập thành công !");
        } else if(UserName.equalsIgnoreCase("") || Password.equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "UserName & Password không để trống");
        } else {
            JOptionPane.showMessageDialog(this, "Username hoặc Password không đúng");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

