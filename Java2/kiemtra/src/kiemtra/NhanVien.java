    private void fillForm() {
        model.setRowCount(0);
        for (NhanVienTest nv : ListNV) {
            model.addRow(new Object[] {nv.getMaNV(),nv.getFullName(),nv.getAge()
                    ,nv.getNgoaiNgu(),nv.getTuoiLD()});
        }
        ClearFrom();
    }
    private NhanVienTest ReadFrom() {
        String MaNV = txtmanv.getText();
        String FullName = txthoten.getText();
        int Age = Integer.parseInt(txttuoi.getText());
        String NgoaiNgu = cbongoaingu.getSelectedItem() + "";
        NhanVienTest nv = new NhanVienTest(MaNV, FullName, Age, NgoaiNgu);
        return nv;
    }
    private void addFrom() {
        NhanVienTest nv = ReadFrom();
        ListNV.add(nv);
    }
    private void showDetails() {
        int select = tblNhanVien.getSelectedRow();
        NhanVienTest nv = ListNV.get(select);
        txtmanv.setText(nv.getMaNV());
        txthoten.setText(nv.getFullName());
        txttuoi.setText(nv.getAge() + "");
        cbongoaingu.setSelectedItem(nv.getNgoaiNgu());
        
    }
    private void Delete() {
        int sl = tblNhanVien.getSelectedRow();
        ListNV.remove(sl);
    }
    private void ClearFrom() {
        txtmanv.setText("");
        txthoten.setText("");
        txttuoi.setText("");
        cbongoaingu.setSelectedIndex(0);
        
    }
