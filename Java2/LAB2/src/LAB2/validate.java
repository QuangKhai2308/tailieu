    private boolean checkNull() {
        if (txtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên chưa được nhập");
            txtName.requestFocus();
            return false;
        } else if (txtAddress.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,"Địa chỉ chưa được nhập");
            txtAddress.requestFocus();
            return false;
        } else if (!chkReading.isSelected() && !chkSinging.isSelected() && !chkDancing.isSelected()) {
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 sở thích của bạn");
            return false;
        } else if (!rdoNam.isSelected() && !rdoNu.isSelected()) {
            JOptionPane.showMessageDialog(this, "Hãy chọn giới tính của bạn");
            return false;
        }
        return true;
    }
    private void ResetForm() {
        txtName.setText("");
        txtAddress.setText("");
        cboQualification.setSelectedIndex(0);
        chkDancing.setSelected(false);
        chkReading.setSelected(false);
        chkSinging.setSelected(false);
        rdoNam.setSelected(false);
        rdoNam.setSelected(false);
        buttonGroup1.clearSelection();

    }
