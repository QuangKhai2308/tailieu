    private void btnWriterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWriterActionPerformed
        //Lấy dữ liệu trên ô text => ghe vào file
        
        try {
            //B1: mở luồng (Tạo đối tượng đọc/ghi)
            FileWriter fw = new FileWriter("a.doc");
            //B2: thực hiện đọc/ghi
            fw.write(txtContent.getText());
            //B3: Đóng luồng
            fw.flush();//xả luồng
            fw.close();
            txtContent.setText("");
            JOptionPane.showMessageDialog(this, "Ghi thành công");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi ghi file");
        }
    }//GEN-LAST:event_btnWriterActionPerformed

    private void txtReaderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtReaderActionPerformed
        //Đọc dữ liệu từ file => hiển thị len txtContent
        try {
            //B1: Mở luồng(Tạo đối tượng đọc/ghi)
            FileReader fr = new FileReader("a.doc");
            //B2: Thực hiên đọc ghi
            int c;
            String Content = "";
            while((c = fr.read()) != -1){
                Content+=(char)c;
            }
            //B3: Đóng luồng
            fr.close();
            txtContent.setText(Content);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi ghi file");
        }
    }//GEN-LAST:event_txtReaderActionPerformed

