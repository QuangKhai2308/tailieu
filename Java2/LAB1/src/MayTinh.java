    private boolean checkNull() {
        if (txtFirst.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập giá trị");
            txtFirst.requestFocus();
            return false;

        } else if (txtSeconds.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập giá trị");
            txtSeconds.requestFocus();
            return false;
        } else {
            return true;
        }
    }
