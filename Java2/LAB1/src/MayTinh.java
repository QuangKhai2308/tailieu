    private void btnCongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCongActionPerformed
        try {
            String Number1 = txtFirst.getText();
            String Number2 = txtSeconds.getText();
            //check so 1
            try {
                Double number1 = Double.parseDouble(Number1);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập số");
                txtFirst.requestFocus();
                return;
            }
            //check so 2
            try {
                Double number2 = Double.parseDouble(Number2);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập số");
                txtSeconds.requestFocus();
                return;
            }
            double sum = 0;
            if (checkNull()) {
                Double number1 = Double.parseDouble(Number1);
                Double number2 = Double.parseDouble(Number2);
                sum = number1 + number2;
                
                txtResult.setText(sum + "");
           }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnCongActionPerformed

