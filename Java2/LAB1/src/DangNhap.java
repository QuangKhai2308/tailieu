    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (txtUsername.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Username chưa được nhập");
                txtUsername.requestFocus();
                return;
            } else if (txtPassword.getText().isEmpty() ){
                JOptionPane.showMessageDialog(this, "Password chưa được nhập");
                txtPassword.requestFocus();
                return;
            } else if (txtComfirm.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Password chưa được nhập");
                txtComfirm.requestFocus();
                return;
            }
            String confirm = txtComfirm.getText();
            String pass = txtPassword.getText();
            if (!pass.equals(confirm)) {
                JOptionPane.showMessageDialog(this, "Mật khẩu không chính xác, vui lòng thử lại");
                reset();
                return;
            } else {
                JOptionPane.showMessageDialog(this, "Đăng nhập thành công!");
                return;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

